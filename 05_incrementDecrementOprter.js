


// index++ ====> index+1 post increment operator
// index-- ====> index-1 post decrement
//++ index ======> pre increment
//-- index ======> pre decrement

var index= 10;
var y = index+1;
console.log(index , y);

var index= 20;
var y = index++;
console.log(index , y);


var index= 20;
var y = index--;
console.log(index , y);
